const legacyIndustrySlugMap: Record<string, string> = {
  "estate-agents": "real-estate",
  "health-wellness-clinics": "medical",
  "legal-services": "law-firms",
  "beauty-aesthetics": "beauty",
  "hospitality-restaurants": "restaurants",
};

export function resolveIndustrySlug(slug: string) {
  return legacyIndustrySlugMap[slug] ?? slug;
}

export function getIndustryBySlug(slug: string) {
  const resolvedSlug = resolveIndustrySlug(slug);
  return industries.find((industry) => industry.slug === resolvedSlug);
}
